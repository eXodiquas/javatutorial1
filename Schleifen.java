class Schleifen {

    public static void main(String[] args) {
        //Do-While Schleife
        int i = 1;

        do{
            System.out.println(i);
            i += 1;
        }
        while(i <= 5);

        //While Schleife
        int j = 1;

        while(j <= 5) {
            System.out.println(j);
            j += 1;
        }

        //For Schleife
        for(int k = 1; k <= 5; k += 1) {
            System.out.println(k);
        }

    }

}